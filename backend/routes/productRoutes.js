import express from 'express'
const router = express.Router()
import {
  getProducts,
  getProductById,
  deleteProduct,
  createProduct,
  updateProduct,
  createProductReview,
  getTopProducts,
} from '../controllers/productController.js'
import { protect, systemAdmin } from '../middleware/authMiddleware.js'

router.route('/').get(getProducts).post(protect, systemAdmin, createProduct)
router.route('/:id/reviews').post(protect, createProductReview)
router.get('/top', getTopProducts)
router
  .route('/:id')
  .get(getProductById)
  .delete(protect, systemAdmin, deleteProduct)
  .put(protect, systemAdmin, updateProduct)

export default router
